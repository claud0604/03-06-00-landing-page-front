// ─── i18n ENGINE ───
const LANG_KEY = 'apl-lang';
const LANG_LABELS = { en: 'EN', ko: 'KO', ja: 'JA', zh: 'ZH' };
const LANG_HTML = { en: 'en', ko: 'ko', ja: 'ja', zh: 'zh' };

function getCurrentLang() {
  return localStorage.getItem(LANG_KEY) || 'en';
}

function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  // Update html lang attribute
  document.documentElement.lang = LANG_HTML[lang] || 'en';

  // Update lang button label
  const langBtn = document.getElementById('langBtn');
  if (langBtn) langBtn.textContent = LANG_LABELS[lang] || 'EN';

  // Update active state in dropdown
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.remove('active');
  });
  const activeOpt = document.querySelector(`.lang-option[onclick="setLang('${lang}')"]`);
  if (activeOpt) activeOpt.classList.add('active');
}

function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  applyTranslations(lang);
  document.getElementById('langDropdown').classList.remove('open');
}

// Init language on page load
document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLang();
  applyTranslations(lang);
});
